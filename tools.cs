        public static uint ToUInt(string ip)
        {
            byte[] bip = IPAddress.Parse(ip).GetAddressBytes();
            Array.Reverse(bip);
            return BitConverter.ToUInt32(bip, 0);
        }
        public static bool IsJapaneseIP(string ip_str)
        {
            uint ip_num = ToUInt(ip_str);
            foreach (var (Start, End) in jp_addr_block) if(Start <= ip_num && ip_num <= End) return true;
            return false;
        }
